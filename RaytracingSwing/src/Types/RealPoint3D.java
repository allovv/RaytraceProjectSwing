package Types;

public class RealPoint3D {
    public double x;
    public double y;
    public double z;

    public RealPoint3D() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    public RealPoint3D(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
